import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      { userAgent: '*', allow: '/', disallow: ['/auth/', '/api/'] },
      { userAgent: 'Googlebot', allow: '/', disallow: ['/auth/', '/api/'] },
    ],
    sitemap: 'https://sakfootball.com/sitemap.xml',
  };
}
